#pragma once


struct Vec3
{
public:

    //-----------------------------------------------------
    // public data
    //-----------------------------------------------------
    union
    {
        float xyz[3]{ 0 };
       
        struct
        {
            float x, y, z;
        };
    };


    //-----------------------------------------------------
    // constructors
    //-----------------------------------------------------
    Vec3() :
        x(0), y(0), z(0) {}

    Vec3(const float x_, const float y_, const float z_) :
        x(x_), y(y_), z(z_) {}

    Vec3(const Vec3& v) :
        x(v.x), y(v.y), z(v.z) {}


    //-----------------------------------------------------
    // operators
    //-----------------------------------------------------

    // assignment
    inline Vec3& operator = (const Vec3& v)
    {
        x = v.x;
        y = v.y;
        z = v.z;
        return *this;
    }

    // comparison
    inline bool operator == (const Vec3& v) const
    {
        return (x == v.x) && (y == v.y) && (z == v.z);
    }

    // negation
    inline Vec3 operator - () const
    {
        return Vec3(-x, -y, -z);
    }
};
